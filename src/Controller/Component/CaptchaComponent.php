    public function generateCaptcha($length=4, $maxDistance=7, $color=Null, $bg_color=Null){
    	$captcha = "";
    	$bg 	 = "ABCDEF";
    	$fg 	 = "";
		$randStr = $this->generateRandomString( $length );
		$this->session->write('captcha', $randStr);
		
		for($c=0;$c<=5;$c++){
			$fg .= rand(0,9);
		}
		$distance = [];
		for($d=0;$d<$length;$d++){
			$distance[$d] = rand(0, $maxDistance);
		}
		$fullspace = ".................................";
		for($j=0;$j<=8;$j++){
			for($i=0;$i<=$length-1;$i++){
				$c = $randStr[$i];
				$space = substr($fullspace, 0, $distance[$i]);
				$captcha .= $space.$this->char[$c][$j].$space;
			}
			$captcha .= "<br>\n";
		}
		$recaptcha = '';
		for($i=0;$i<=strlen($captcha)-1;$i++){
			if($captcha[$i]!='<' && $captcha[$i]!='b' && $captcha[$i]!='r' && $captcha[$i]!='>' && $captcha[$i]!="\n"){
				//for($c=0;$c<=5;$c++){
				//	$bgColor .= $bg[rand(0,5)];
				//	$fgColor .= $fg[rand(0,4)];
				//}
				$bgColor = "#fff";
				$bgColor = "red";
				if(isset($bgColor)){
					$bgColor = $bgColor;
				}				
				$fgColor = "#000";
				$fgColor = "white";
				if(isset($color)){
					$fgColor = $color;
				}
				if($captcha[$i]=="."){				
					//$randChar = $this->generateRandomString( 1 );	
					$randChar = " ";
					$randChar = "•";
					$randChar = "■";
					$recaptcha .= '<span style="color:'.$bgColor.';-webkit-transform:scale(1.0, 1.0);-moz-transform:scale(1.0, 1.0);-ms-transform:scale(1.0, 1.0);-o-transform:scale(1.0, 1.0);transform:scale(2.0,1.0);">'.$randChar.'</span>';
				}
				if($captcha[$i]=="#"){
					//$randChar = $this->generateRandomString( 1 );
					$randChar = ".";
					$randChar = "•";
					$randChar = "■";
					$randChar = "•";
					//$recaptcha .= '<span style="color:'.$fgColor.';">'.$randChar.'</span>';
